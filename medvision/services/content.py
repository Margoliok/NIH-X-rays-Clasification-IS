@lru_cache(maxsize=1)
def _load_diseases():
    with DISEASES_PATH.open("r", encoding="utf-8") as handle:
        return json.load(handle)


def get_supported_languages():
    return SUPPORTED_LANGUAGES


def get_ui_strings(lang):
    return UI_TEXTS.get(lang, UI_TEXTS["kk"])


def get_system_overview(lang):
    return SYSTEM_OVERVIEW.get(lang, SYSTEM_OVERVIEW["kk"])


def _localize_disease(disease, lang):
    block = disease.get(lang, disease["kk"])
    return {
        "slug": disease["slug"],
        "model_label": disease["model_label"],
        "title": block["title"],
        "description": block["description"],
        "short_description": block["short_description"],
        "symptoms": block["symptoms"],
        "risks": block["risks"],
        "what_to_do": block["what_to_do"],
        "when_to_see_doctor": block["when_to_see_doctor"],
    }


def get_disease_catalog(lang):
    return [_localize_disease(disease, lang) for disease in _load_diseases()]


def get_disease_by_slug(slug, lang):
    for disease in _load_diseases():
        if disease["slug"] == slug:
            return _localize_disease(disease, lang)
    return None


def get_disease_by_label(label, lang):
    normalized_label = label.replace(" ", "_")
    for disease in _load_diseases():
        model_label = disease["model_label"]
        if model_label == label or model_label.replace(" ", "_") == normalized_label:
            return _localize_disease(disease, lang)
    return None
