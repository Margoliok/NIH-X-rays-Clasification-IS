def resolve_advice_key(primary_label, probability, is_healthy):
    if is_healthy:
        return "healthy_follow_up"
    if primary_label in {"Pneumothorax", "Edema"} and probability >= 0.72:
        return "urgent_review"
    if probability < 0.5:
        return "repeat_check"
    return "medical_consult"


